// Mouse speed state (used in process_record_user)
static uint8_t mouse_speed = 5;   // default mid speed

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {

        // в”Җв”Җ Mouse speed в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
        case MS_SLOW:
            if (record->event.pressed) {
                mouse_speed = 1;
            }
            return false;
        case MS_FAST:
            if (record->event.pressed) {
                mouse_speed = 10;
            } else {
                mouse_speed = 5;   // reset on release
            }
            return false;

        // в”Җв”Җ Niri workspace switch (Super+1..9) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
        case NR_WS1: if (record->event.pressed) tap_code16(LGUI(KC_1)); return false;
        case NR_WS2: if (record->event.pressed) tap_code16(LGUI(KC_2)); return false;
        case NR_WS3: if (record->event.pressed) tap_code16(LGUI(KC_3)); return false;
        case NR_WS4: if (record->event.pressed) tap_code16(LGUI(KC_4)); return false;
        case NR_WS5: if (record->event.pressed) tap_code16(LGUI(KC_5)); return false;
        case NR_WS6: if (record->event.pressed) tap_code16(LGUI(KC_6)); return false;
        case NR_WS7: if (record->event.pressed) tap_code16(LGUI(KC_7)); return false;
        case NR_WS8: if (record->event.pressed) tap_code16(LGUI(KC_8)); return false;
        case NR_WS9: if (record->event.pressed) tap_code16(LGUI(KC_9)); return false;

        // в”Җв”Җ Niri move window to workspace (Super+Shift+1..5) в”Җ
        case NR_MW1: if (record->event.pressed) tap_code16(LGUI(LSFT(KC_1))); return false;
        case NR_MW2: if (record->event.pressed) tap_code16(LGUI(LSFT(KC_2))); return false;
        case NR_MW3: if (record->event.pressed) tap_code16(LGUI(LSFT(KC_3))); return false;
        case NR_MW4: if (record->event.pressed) tap_code16(LGUI(LSFT(KC_4))); return false;
        case NR_MW5: if (record->event.pressed) tap_code16(LGUI(LSFT(KC_5))); return false;

        default:
            return true;
    }
}

// в”Җв”Җ Tapping-term per-key в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// Home-row mods need a longer tapping term to avoid accidental triggers
// during fast typing. Index fingers (F/J) are slightly shorter since
// they move more precisely.
uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        // Pinkies вҖ” slowest fingers, longest term
        case HM_A:
        case HM_SC:
            return 220;
        // Ring fingers
        case HM_S:
        case HM_L:
            return 200;
        // Middle fingers
        case HM_D:
        case HM_K:
            return 185;
        // Index fingers вҖ” faster
        case HM_F:
        case HM_J:
            return 170;
        // Thumb keys
        case TH_SPC:
        case TH_BSP:
        case TH_TAB:
        case TH_ENT:
            return 180;
        default:
            return TAPPING_TERM;
    }
}

// в”Җв”Җ Permissive hold per-key в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// Enable permissive hold only for thumb keys (faster layer activation),
// but NOT for home-row mods (avoids mis-triggers during rolls).
bool get_permissive_hold(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case TH_SPC:
        case TH_BSP:
        case TH_TAB:
        case TH_ENT:
            return true;
        default:
            return false;
    }
}

